//串口移位输出, SPI方式写入8bit 数据
/*************************************************/

void write(unsigned char dat)
 {  unsigned char s,temp;
    int i;
    sclk=0;
    s=dat;

    for(i=8;i>0;i--)
      {sclk=0;
       nop();
       nop();
       temp=s & 0x80;                   //先送高位，再送低位
       if(temp)
            {di=1;}
       else {di=0;}

       sclk=1;   
       s=s<<1;  
      }
 } 

